    EXPIRATION_DATE  DATE NOT NULL,
    RECEIVED_DATE    DATE NOT NULL,
    CONSTRAINT FK_BATCH_MEDICINE FOREIGN KEY ( MEDICINE_ID )
        REFERENCES MEDICINE ( MEDICINE_ID ),
    CONSTRAINT FK_BATCH_SUPPLIER FOREIGN KEY ( SUPPLIER_ID )
        REFERENCES SUPPLIER ( SUPPLIER_ID ),
    CONSTRAINT FK_BATCH_PHARMACY FOREIGN KEY ( PHARMACY_ID )
        REFERENCES PHARMACY ( PHARMACY_ID ),
    CONSTRAINT UK_BATCH_UNIQUE UNIQUE ( MEDICINE_ID,
                                        PHARMACY_ID,
                                        BATCH_NUMBER ),
    CONSTRAINT CHK_BATCH_PURCHASE_PRICE CHECK ( PURCHASE_PRICE >= 0 ),
    CONSTRAINT CHK_BATCH_CURRENT_QUANTITY CHECK ( CURRENT_QUANTITY >= 0 ),
    CONSTRAINT CHK_BATCH_DATES CHECK ( EXPIRATION_DATE > RECEIVED_DATE )
